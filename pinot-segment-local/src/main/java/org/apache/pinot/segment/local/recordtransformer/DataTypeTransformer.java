package org.apache.pinot.segment.local.recordtransformer;

import java.util.HashMap;
import java.util.Map;
import java.util.Set;
import org.apache.pinot.common.utils.ThrottledLogger;
import org.apache.pinot.segment.local.utils.DataTypeTransformerUtils;
import org.apache.pinot.spi.config.table.TableConfig;
import org.apache.pinot.spi.config.table.ingestion.IngestionConfig;
import org.apache.pinot.spi.data.FieldSpec;
import org.apache.pinot.spi.data.Schema;
import org.apache.pinot.spi.data.readers.GenericRow;
import org.apache.pinot.spi.recordtransformer.RecordTransformer;
import org.apache.pinot.spi.utils.PinotDataType;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;


/**
 * The {@code DataTypeTransformer} class converts column values to follow the configured {@link PinotDataType}s. It has
 * two usages:
 * <ul>
 *   <li>Schema columns: constructed from the {@link Schema}, it should run after all the values have been generated by
 *   other transformers (such as {@link ExpressionTransformer}) so that all values follow the data types in
 *   {@link FieldSpec}.</li>
 *   <li>Source fields: constructed from an explicit column-to-{@link PinotDataType} map (see
 *   {@code IngestionConfig.sourceFieldConfigs}), it runs before other transformers (such as
 *   {@link ExpressionTransformer}) to fix the data types of the source fields they consume.</li>
 * </ul>
 */
public class DataTypeTransformer implements RecordTransformer {
  private static final Logger LOGGER = LoggerFactory.getLogger(DataTypeTransformer.class);

  private final Map<String, PinotDataType> _dataTypes;
  private final boolean _continueOnError;
  private final ThrottledLogger _throttledLogger;

  /// Creates a [DataTypeTransformer] that converts the (non-virtual) schema columns to the data types defined in the
  /// [Schema].
  public DataTypeTransformer(TableConfig tableConfig, Schema schema) {
    this(tableConfig, extractSchemaDataTypes(schema));
  }

  /// Creates a [DataTypeTransformer] that converts the given columns to the provided [PinotDataType]s. This is useful
  /// for fixing the data types of source fields before other transformers (such as [ExpressionTransformer]) consume
  /// them.
  public DataTypeTransformer(TableConfig tableConfig, Map<String, PinotDataType> dataTypes) {
    _dataTypes = dataTypes;
    IngestionConfig ingestionConfig = tableConfig.getIngestionConfig();
    _continueOnError = ingestionConfig != null && ingestionConfig.isContinueOnError();
    _throttledLogger = new ThrottledLogger(LOGGER, ingestionConfig);
  }

  private static Map<String, PinotDataType> extractSchemaDataTypes(Schema schema) {
    Map<String, PinotDataType> dataTypes = new HashMap<>();
    for (FieldSpec fieldSpec : schema.getAllFieldSpecs()) {
      if (!fieldSpec.isVirtualColumn()) {
        dataTypes.put(fieldSpec.getName(), PinotDataType.getPinotDataTypeForIngestion(fieldSpec));
      }
    }
    return dataTypes;
  }

  @Override
  public boolean isNoOp() {
    return _dataTypes.isEmpty();
  }

  @Override
  public Set<String> getInputColumns() {
    return _dataTypes.keySet();
  }

  @Override
  public void transform(GenericRow record) {
    for (Map.Entry<String, PinotDataType> entry : _dataTypes.entrySet()) {
      String column = entry.getKey();
      try {
        Object value = record.getValue(column);
        value = DataTypeTransformerUtils.transformValue(column, value, entry.getValue());
        record.putValue(column, value);
      } catch (Exception e) {
        if (!_continueOnError) {
          throw new RuntimeException("Caught exception while transforming data type for column: " + column, e);
        }
        _throttledLogger.warn("Caught exception while transforming data type for column: " + column, e);
        record.putValue(column, null);
        record.markIncomplete();
      }
    }
  }
}
